# Integer
a = 10
print(type(a))

# Float
a = 10.3
print(type(a))

# String
a = "jayesh"
print(type(a))

# Boolean
is_student = True
print(type(is_student))

# List
a = ["jayesh"]
print(type(a))

# Tuple
a = (10, 20, 30)
print(type(a))

# Dictionary
a = {"jayesh": "rushi"}
print(type(a))

# Set
a = {"jayesh"}
print(type(a))